// ─── ЗАГРУЗКА В FIRESTORE ─────────────────────────────────────────────────────

async function seed() {
  console.log("Начинаем загрузку данных в Firestore...\n");

  // Услуги
  console.log(`Загружаем ${services.length} услуг...`);
  const servicesBatch = db.batch();
  for (const service of services) {
    const ref = db.collection("services").doc();
    servicesBatch.set(ref, service);
  }
  await servicesBatch.commit();
  console.log("✓ Услуги загружены\n");

  // Новости
  console.log(`Загружаем ${news.length} новостей...`);
  const newsBatch = db.batch();
  for (const item of news) {
    const ref = db.collection("news").doc();
    newsBatch.set(ref, item);
  }
  await newsBatch.commit();
  console.log("✓ Новости загружены\n");

  console.log("Готово! Все данные успешно записаны в Firestore.");
  process.exit(0);
}

seed().catch((err) => {
  console.error("Ошибка:", err);
  process.exit(1);
});
